public class Switch {
    public static void main(String[] args){
        int poraRoku = 1;

        switch(poraRoku){
            case 1:
                System.out.println("wiosna");
                break;
            case 2:
                System.out.println("lato");
                break;
            case 3:
                System.out.println("jesien");
                break;
            case 4:
                System.out.println("zima");
                break;
            default:
                System.out.println("inna plameta");
        }
    }
}
